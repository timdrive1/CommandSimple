public class GarageDoor {

    public GarageDoor() {
    }

    public void up() {
        System.out.println("открыта дверь гаража");
    }

    public void down() {
        System.out.println("закрыта дверь гаража");
    }

    public void stop() {
        System.out.println("стоп дверь гаража");
    }

    public void lightOn() {
        System.out.println("свет в гараже вкл");
    }

    public void lightOff() {
        System.out.println("свет в гараже выкл");
    }
}